export function getStockById(id: string) {
  return mockStocks.find((s) => s.id === id);
}

export function getStockBySymbol(symbol: string) {
  return mockStocks.find((s) => s.symbol === symbol);
}

export function getLivePrice(stockId: string) {
  return mockLivePrices[stockId] ?? null;
}

export function getRecommendationById(id: string) {
  return mockRecommendations.find((r) => r.id === id);
}

export function getActiveRecommendations() {
  return mockRecommendations.filter((r) => r.status === "active");
}

export function getClosedRecommendations() {
  return mockRecommendations.filter((r) => r.status !== "active");
}

export function getOpenHoldings() {
  return mockHoldings.filter((h) => h.status === "open");
}

export function getUnreadNotifications() {
  return mockNotifications.filter((n) => !n.isRead);
}

export function getPortfolioSummary() {
  const openHoldings = getOpenHoldings();

  let totalInvested = 0;
  let currentValue = 0;
  let todaysPnl = 0;

  for (const holding of openHoldings) {
    const price = mockLivePrices[holding.stockId];
    if (!price) continue;

    totalInvested += holding.investedAmount;
    currentValue += holding.quantity * price.ltp;
    todaysPnl += holding.quantity * price.change;
  }

  const totalPnl = currentValue - totalInvested;
  const totalPnlPercent = totalInvested > 0 ? (totalPnl / totalInvested) * 100 : 0;
  const todaysPnlPercent = totalInvested > 0 ? (todaysPnl / totalInvested) * 100 : 0;

  return {
    totalInvested: Math.round(totalInvested * 100) / 100,
    currentValue: Math.round(currentValue * 100) / 100,
    totalPnl: Math.round(totalPnl * 100) / 100,
    totalPnlPercent: Math.round(totalPnlPercent * 100) / 100,
    todaysPnl: Math.round(todaysPnl * 100) / 100,
    todaysPnlPercent: Math.round(todaysPnlPercent * 100) / 100,
    holdingsCount: openHoldings.length,
    activeRecommendations: getActiveRecommendations().length,
  };
}
